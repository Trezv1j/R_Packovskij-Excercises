let number = 100; // Starting number

// Loop to iterate through numbers from 100 to 199
for (let i = 0; i < 100; i++) {
    // Checking if the number is divisible by 3
    if (number % 3 === 0) {
        console.log(number); // If divisible by 3, print the number
    }
    number++; // Increment the number for the next iteration
}